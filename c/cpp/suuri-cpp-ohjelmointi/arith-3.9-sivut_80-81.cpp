// arith.cpp - some C++ arithmetic
#include <iostream>
using namespace std;
main()
{
	float hats, heads;
	cout << "Enter a number: ";
	cin >> hats;
	cout << "Enter another number: ";
	cin >> heads;

	cout << "hats = " << hats << "; heads = " << heads << "\n";
	cout << "hats + heads = " << hats + heads << "\n";
	cout << "hats - heads = " << hats - heads << "\n";
	cout << "hats * heads = " << hats * heads << "\n";
	cout << "hats / heads = " << hats / heads << "\n";
}
